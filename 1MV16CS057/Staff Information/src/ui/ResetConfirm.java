    private void JBtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnSubmitActionPerformed
        //submit button

        int f1 = JTxtUname.getDocument().getLength();
        int f2 = JTxtPhone.getDocument().getLength();

        if (f1 == 0 && f2 == 0) {
            JOptionPane.showMessageDialog(null, "Please provide the 'User Name' and 'Phone no'");
        } else if (f1 == 0) {
            JOptionPane.showMessageDialog(null, "Please provide the 'User Name'");
        } else if (f2 == 0) {
            JOptionPane.showMessageDialog(null, "Please provide the 'Phone no'");
        } else if (!RdoAdmin.isSelected() && !Rdoteacher.isSelected()) {

            JOptionPane.showMessageDialog(null, "Please Select either Admin or Teacher");
        } else if (RdoAdmin.isSelected()) {
            // JOptionPane.showMessageDialog(null, "Admin Selected");

            SQLDAO obj = new SQLDAO();
            try {
                
               ResultSet rs =obj.checkForResetAdmin(JTxtUname.getText(), Long.valueOf(JTxtPhone.getText()));
                if (rs.next()) {
                    new NewUser("ADMIN",JTxtUname.getText(),Long.valueOf(JTxtPhone.getText()),true).setVisible(true);
                    dispose();
                }else{
                    //credentials not found
                    JOptionPane.showMessageDialog(null, "credentials not found");
                    JTxtUname.setText("");
                    JTxtPhone.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ResetConfirm.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            //JOptionPane.showMessageDialog(null, "Teacher Selected");

            SQLDAO obj = new SQLDAO();
            try {
                ResultSet rs =obj.checkForResetTeacher(JTxtUname.getText(), Long.valueOf(JTxtPhone.getText()));
                if (rs.next()) {
                    
                    new NewUser("TEACHER",JTxtUname.getText(),Long.valueOf(JTxtPhone.getText()),true).setVisible(true);
                    dispose();
                    
                }else{
                    //credentials not found
                    JOptionPane.showMessageDialog(null, "credentials not found");
                    JTxtUname.setText("");
                    JTxtPhone.setText("");
                }
            } catch (SQLException ex) {
                Logger.getLogger(ResetConfirm.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_JBtnSubmitActionPerformed

